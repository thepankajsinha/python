#Write a python program to find an average of two floating numbers entered by the user.

a = float(input("Enter number 1: "))
b = float(input("Enter number 2: "))

avg = (a + b) / 2

print(f"The avg of {a} and {b} is", avg)

#Output
# Enter number 1: 10
# Enter number 2: 11 
# The avg of 10.0 and 11.0 is 10.5