#List slicing

#Create a list of numbers
list_of_numbers = [1, 2, 3, 4, 5]

#Slicing the list from index 0 to index 3
print(list_of_numbers[0:len(list_of_numbers)]) # Output: [1, 2, 3, 4, 5]
print(list_of_numbers[0:3])  # Output: [1, 2, 3]
print(list_of_numbers[:4])  # Output: [1, 2, 3, 4]
print(list_of_numbers[0:])  # Output: [1, 2, 3, 4, 5]

