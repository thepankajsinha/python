#List is a built in data structure that is used to store multiple items in a single variable.
#List items can be of any data type.

# Characteristics of Lists:
# Mutable: Elements can be modified, added, or removed after the list is created.
# Ordered: Elements have a defined order, and this order is preserved.
# Indexable: You can access elements by their index, starting from 0.
# Dynamic Size: Lists can grow or shrink in size as needed.



