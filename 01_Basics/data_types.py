# In python , data types are automatically identified by the Python interpreter

name = 'John Doe' # String
math_score = 99 # Integer
gpa = 3.75 # Float
is_passed = True # Boolean

#none value
none_value = None # NoneType

#complex number
complex_number = 3 + 4j



# type() function is used to determine the data type
print(type(name))          # <class 'str'>
print(type(math_score))    # <class 'int'>
print(type(gpa))          # <class 'float'>
print(type(is_passed))     # <class 'bool'>
print(type(none_value))    # <class 'NoneType'>
print(type(complex_number)) # <class 'complex'>
