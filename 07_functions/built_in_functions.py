#abs
print(abs(-10))  # Output: 10

# round
print(round(3.14159, 2))  # Output: 3.14

# max
print(max(1, 2, 3, 4, 5))  # Output: 5

# min
print(min(1, 2, 3, 4, 5))  # Output: 1

# sum
print(sum([1, 2, 3, 4, 5]))  # Output: 15

# len
print(len("Hello, World!"))  # Output: 13

#divmod
print(divmod(10, 3))  # Output: (3, 1) (10 divided by 3 is 3 with a remainder of 1)

# pow
print(pow(2, 3))  # Output: 8 (2 raised to the power of 3)

# sorted
print(sorted([5, 2, 3, 1, 4]))  # Output: [1, 2, 3, 4, 5] (sorted list)

# type
print(type(42))  # Output: <class 'int'> (type of the variable

