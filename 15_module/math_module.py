import math

# This module provides basic mathematical operations

#ceil
print(math.ceil(4.2))  # Output: 5

#floor
print(math.floor(4.8))  # Output: 4

#sqrt
print(math.sqrt(16))  # Output: 4.0

#factorial
print(math.factorial(5))  # Output: 120

#gcd
print(math.gcd(12, 15))  # Output: 3 (greatest common divisor)

#pow
print(math.pow(2, 3))  # Output: 8.0 (2 raised to the power of 3)

#log
print(math.log(100, 10))  # Output: 2.0 (logarithm base 10 of 100)

#fabs
print(math.fabs(-5.5))  # Output: 5.5 (absolute value of -5.5)

#fmod
print(math.fmod(5, 2))  # Output: 1.0 (5 modulo 2)

#sin
print(math.sin(math.pi / 2))  # Output: 1.0 (sine of 90 degrees)