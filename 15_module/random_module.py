import random

# random real integer between 0 to 1
print(random.random())  # Output: 0.854666505471929


# random integer between 1 and 10
print(random.randint(1, 10))  # Output: 7


# random choice from a list
choices = ['apple', 'banana', 'cherry']
print(random.choice(choices))  # Output: 'banana'

