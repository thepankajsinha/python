#data types in Python

#1 Numbers - Integer, Float, Complex, Boolean
# Integers
int_var = 42

# Floats
float_var = 3.14

# Complex numbers
complex_var = 2 + 3j

# Booleans
bool_var = True




#2 Sequences - String, List, Tuple

# Strings
string_var = "Hello, World!"

# Lists
list_var = [1, 2, 3, "four", 5.0]

# Tuples
tuple_var = (1, 2, 3, "four", 5.0)





#3 Sets
set_var = {1, 2, 3, "four", 5.0}  # A set of unique elements

#4 Mappings - Dictionary
dict_var = {
    "name": "Alice",
    "age": 30,
    "is_student": False
}




#5 NoneType
none_var = None  # Represents the absence of a value

