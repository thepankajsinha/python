# In python , data types are automatically identified by the Python interpreter
# type() function is used to determine the data type

# Integer
x = 10
print(type(x))  # Output: <class 'int'>


# Float
y = 5.555
print(type(y))  # Output: <class 'float'>


# String
s = "Hello, World!"
print(type(s))  # Output: <class 'str'>


# Boolean
b = True
print(type(b))  # Output: <class 'bool'>


# none
n = None
print(type(n))  # Output: <class 'NoneType'>