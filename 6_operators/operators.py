#Logical operators
a = 10
b = 5
c = 2

# "and" Returns True if both operands are True
ans = (a > b) and (a > c)
print(ans)  #True


# "or" Returns True if either operand is True
result = (a > b) or (c > b)
print(result)  #True


# "not" Returns True if operand is False
d = True
print(not d)  # Output: False
