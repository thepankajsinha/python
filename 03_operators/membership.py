# Membership operators are used to check if a value is a member of the given sequence or not.

# in
fruits = ['apple', 'banana', 'cherry']

print('apple' in fruits)  # Output: True (apple is in the list)
print('orange' in fruits)  # Output: False (orange is not in the list)


# not in
print('banana' not in fruits)  # Output: False (banana is in the list)
print('grape' not in fruits)  # Output: True (grape is not in the list)