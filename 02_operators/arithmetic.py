# Arithmetic operators

x = 10
y = 4

print( x + y)  # Output: 14
print(x - y)   # Output: 6   
print(x * y)   # Output: 40
print(x / y)   # Output: 2.5

#floor division
#it gives the digit before the decimal point
print(x // y)  #  Output: 2

# Modulus operator
# it gives the remainder of the division
print(x % y)   # Output: 2

# Exponentiation operator
# it raises x to the power of y
print(x ** y)  # Output: 10000