#Typecasting means conversion of variables from one data type to another data type


# Convert a string to an integer
#int() is used to convert any data type to an integer

a = "20"
print(type(a)) #Output <class 'str'>

b = int(a)
print(type(b)) #Output <class 'int'>


# Convert an integer to string
#str() is used to convert any data type to string
e = 10
print(type(e)) #Output <class 'int'>

f = str(e)
print(type(f)) #Output <class 'str'>


