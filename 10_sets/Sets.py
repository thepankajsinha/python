#Sets is the collection of the unordered items
#Each element in the set must be unique and immutable(unchangeable)


#create a sets
set_example = {1, 2, 3, 4, 5}
set_example2 = {1, 2, 3, 4, 5, "apple", "orange"}


#print the set
print(set_example) # Output: {1, 2, 3, 4, 5}
print(set_example2) # Output: {1, 2, 3, 4, 5, 'apple', 'orange'}


#length of the set
print(len(set_example))  # Output: 5


collection = {1,2,2,3,3,4,5,6,7}
print(collection) # Output: {1, 2, 3, 4, 5, 6, 7}
print(len(collection)) # Output:7